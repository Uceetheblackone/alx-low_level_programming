

/**
 * add - functions that finds the sum of two numbers.
 * @a: input value
 * @b: input value
 * Return: the sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - functions that finds the difference of two numbers.
 * @a: input value
 * @b: input value
 * Return: the difference of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - functions that finds the multiple of two numbers.
 * @a: input value
 * @b: input value
 * Return: the multiple of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - functions that finds the division of two numbers.
 * @a: input value
 * @b: input value
 * Return: the division of a and b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - functions that finds the modulus of two numbers.
 * @a: input value
 * @b: input value
 * Return: the modulus of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}
